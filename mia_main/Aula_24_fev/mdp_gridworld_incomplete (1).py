# Mapeamento de "desvio lateral" relativo a cada ação
SLIP_LEFT = {"U": "L", "D": "R", "L": "D", "R": "U"}
SLIP_RIGHT = {"U": "R", "D": "L", "L": "U", "R": "D"}

@dataclass(frozen=True)
@dataclass(frozen=True)
class GridworldStochastic(Gridworld):
    """Com prob 0.8 a ação pretendida ocorre;
       com prob 0.1 escorrega para a esquerda relativa;
       com prob 0.1 escorrega para a direita relativa."""

    def transitions(self, state, action):
        """Devolve lista de (prob, next_state, reward, done)."""
        if self.is_terminal(state):
            return [(1.0, state, 0.0, True)]
        outcomes = [
            (0.8, action),
            (0.1, SLIP_LEFT[action]),
            (0.1, SLIP_RIGHT[action]),
        ]
        result = []
        for prob, a in outcomes:
            dr, dc = ACTION_TO_DELTA[a]
            nr = state[0] + dr
            nc = state[1] + dc
            if 0 <= nr < self.n_rows and 0 <= nc < self.n_cols:
                ns = (nr, nc)
            else:
                ns = state
            reward = self.step_reward
            done = self.is_terminal(ns)
            result.append((prob, ns, reward, done))
        return result

    def step(self, state, action):
        """Amostragem determinística (usada para greedy_policy_from_V e display);
           para DP usamos transitions()."""
        # Usa o modo determinístico (probabilidade máxima) para visualização
        return super().step(state, action)

def bellman_optimality_update_stochastic(env: GridworldStochastic, V, s, gamma):
    if env.is_terminal(s):
        return 0.0
    best = -np.inf
    for a in ACTIONS:
        q_sa = sum(p * (r + gamma * V[ns[0], ns[1]])
                   for p, ns, r, done in env.transitions(s, a))
        best = max(best, q_sa)
    return best

def value_iteration_stochastic(env, gamma, theta=1e-6, max_iters=10_000):
    V = zeros_V(env)
    for it in range(max_iters):
        delta = 0.0
        V_old = V.copy()
        for s in env.states():
            v_new = bellman_optimality_update_stochastic(env, V_old, s, gamma)
            delta = max(delta, abs(v_new - V[s[0], s[1]]))
            V[s[0], s[1]] = v_new
        if delta < theta:
            return V, it + 1
    return V, max_iters

def greedy_policy_stochastic(env, V, gamma):
    pi = {}
    for s in env.states():
        if env.is_terminal(s):
            pi[s] = "·"
            continue
        best_a, best_q = None, -np.inf
        for a in ACTIONS:
            q = sum(p * (r + gamma * V[ns[0], ns[1]])
                    for p, ns, r, done in env.transitions(s, a))
            if q > best_q:
                best_q, best_a = q, a
        pi[s] = best_a
    return pi


