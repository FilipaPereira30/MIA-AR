def bellman_expectation_update(
    env: Gridworld, V: np.ndarray, policy: Dict[Tuple[int,int], Dict[str,float]],
    state: Tuple[int,int], gamma: float
) -> float:
    """Return the updated V(s) using the Bellman expectation backup."""
    if env.is_terminal(state):
        return 0.0

    # TODO 2: compute expected value over actions under pi(a|s)
    # Hints:
    #  - loop over actions
    v_new = 0.0
    # --- YOUR CODE STARTS HERE ---
    for a in ACTIONS:
        pi_a = policy[state][a]          # π(a|s)
        if pi_a == 0.0:
            continue
        ns, r, done = env.step(state, a) # s', r  (determinístico)
        v_new += pi_a * (r + gamma * V[ns[0], ns[1]])
    # --- YOUR CODE ENDS HERE ---
    return v_new


def policy_evaluation(
    env: Gridworld,
    policy: Dict[Tuple[int,int], Dict[str,float]],
    gamma: float,
    theta: float = 1e-6,
    max_iters: int = 10_000,
) -> Tuple[np.ndarray, int]:
    """Iterative policy evaluation."""
    V = zeros_V(env)

    for it in range(max_iters):
        delta = 0.0

        # TODO 3: sweep all states and update V in-place (or into a copy)
        # Use delta = max(delta, |V_new - V_old|). Stop if delta < theta.
        # --- YOUR CODE STARTS HERE ---
        for s in env.states():
            v_old = V[s[0], s[1]]
            v_new = bellman_expectation_update(env, V, policy, s, gamma)
            V[s[0], s[1]] = v_new
            delta = max(delta, abs(v_new - v_old))

        if delta < theta:
            return V, it + 1
        # --- YOUR CODE ENDS HERE ---

    return V, max_iters

