        # (bate na parede) — mas continua a pagar o custo de -1
        if 0 <= new_row < self.n_rows and 0 <= new_col < self.n_cols:
            next_state = (new_row, new_col)
        else:
            next_state = state  # fica no mesmo sítio

        reward = self.step_reward          # -1 por cada passo
        done_flag = self.is_terminal(next_state)
        # --- YOUR CODE ENDS HERE ---

        return next_state, reward, done_flag
    
