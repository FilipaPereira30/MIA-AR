def greedy_policy_from_V(env: Gridworld, V: np.ndarray, gamma: float) -> Dict[Tuple[int,int], str]:
    pi_greedy = {}
    for s in env.states():
        if env.is_terminal(s):
            pi_greedy[s] = "·"
            continue

        # TODO 4: get the best 'a' and 'q' (q is a one-step lookahead action value)
        # Hint: q(s, a) = r(s, a) + gamma * V(s')
        # --- YOUR CODE STARTS HERE ---
        best_a = None
        best_q = -np.inf
        for a in ACTIONS:
            ns, r, done = env.step(s, a)
            q = r + gamma * V[ns[0], ns[1]]   # q(s,a) = R_s^a + γ·V(s')
            if q > best_q:
                best_q = q
                best_a = a

        pi_greedy[s] = best_a
        # --- YOUR CODE ENDS HERE ---
    return pi_greedy

